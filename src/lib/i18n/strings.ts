/**
 * Get native display name for ANY locale code.
 * Uses our curated list first, falls back to Intl.DisplayNames (browser/Node built-in).
 * e.g. "am" → "አማርኛ", "sw" → "Kiswahili"
 */
export function getLocaleDisplayName(code: string): string {
  if (LOCALE_DISPLAY_NAMES[code]) return LOCALE_DISPLAY_NAMES[code];
  try {
    const dn = new Intl.DisplayNames([code], { type: "language" });
    return dn.of(code) || code;
  } catch {
    return code;
  }
}
