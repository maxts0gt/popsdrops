/**
 * Known RTL locales — covers all major RTL scripts.
 * For truly unknown locales, isRTLLocale() also tries Intl.Locale.getTextInfo().
 */
const KNOWN_RTL = new Set(["ar", "he", "fa", "ur", "ps", "sd", "yi", "dv", "ku", "ckb", "ug"]);

/**
 * Detect if a locale is RTL. Checks known set first,
 * then Intl.Locale.getTextInfo() (Chrome 99+, Node 21+).
 */
export function isRTLLocale(locale: string): boolean {
  if (KNOWN_RTL.has(locale)) return true;
  try {
    const intlLocale = new Intl.Locale(locale) as Intl.Locale & {
      getTextInfo?: () => { direction: string };
    };
    if (typeof intlLocale.getTextInfo === "function") {
      return intlLocale.getTextInfo().direction === "rtl";
    }
  } catch {
    // Not a valid locale or API not available
  }
  return false;
}

/** Backward-compatible set — prefer isRTLLocale() for dynamic detection */
export const RTL_LOCALES = KNOWN_RTL;

